#!/usr/bin/env bash
# Generate a Compose snippet that declares N robot containers, each with an
# explicit ROBOT_ID env var. Used by run_scaling_matrix.sh for per-container
# fleet mode (pass3).
#
# Usage:
#   gen_robots_compose.sh <num_robots> <output_file>
#
# Required env at apply-time (interpolated by `docker compose -f`):
#   BAG_PATH  — host path to the rosbag2 directory; mounted read-only into each
#               container at /data/bag.
set -euo pipefail

N="${1:?usage: gen_robots_compose.sh <num_robots> <output_file>}"
OUT="${2:?usage: gen_robots_compose.sh <num_robots> <output_file>}"

cat > "${OUT}" <<'HEADER'
# Auto-generated by gen_robots_compose.sh — one service per robot.
services:
HEADER

for ((i = 1; i <= N; i++)); do
    cat >> "${OUT}" <<EOF
  robot_${i}:
    image: ros2-scaling-robot:local
    network_mode: host
    ipc: host
    environment:
      ROBOT_ID: "${i}"
      BAG_PATH: "/data/bag"
      RATE_HZ: "10"
      ROS_DOMAIN_ID: "42"
      MSG_TYPE: "\${MSG_TYPE:-navsatfix}"
    volumes:
      - "\${BAG_PATH:?BAG_PATH is required}:/data/bag:ro"
    depends_on:
      - sink
EOF
done

echo "[gen_robots_compose] wrote ${N} robot services to ${OUT}"
