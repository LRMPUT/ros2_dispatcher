#!/usr/bin/env bash
# Generate a Compose snippet that declares N robot containers.
#
# Two topologies are supported:
#
#   GATEWAY (default):   each robot container runs only the publisher
#                        (robot_replay.py); a central `sink` service in
#                        compose.kafka.yml / compose.mqtt.yml subscribes to
#                        all robots' DDS topics and produces to the broker.
#
#   EDGE:                each robot container ALSO runs its own private
#                        kafka_sink / mosquitto_sink, so each robot acts as
#                        an independent edge node publishing directly to
#                        the broker.  Set TOPOLOGY=edge to enable.
#
# Usage:
#   gen_robots_compose.sh <num_robots> <output_file>
#
# Required env (interpolated by `docker compose -f` at apply time):
#   BAG_PATH    — host path to the rosbag2 directory (mounted read-only).
# Optional env (consumed here at generation time):
#   TOPOLOGY    — 'gateway' (default) or 'edge'.
#   SINK_KIND   — 'kafka' (default) or 'mqtt'; only used in edge topology.
set -euo pipefail

N="${1:?usage: gen_robots_compose.sh <num_robots> <output_file>}"
OUT="${2:?usage: gen_robots_compose.sh <num_robots> <output_file>}"
TOPOLOGY="${TOPOLOGY:-gateway}"
SINK_KIND="${SINK_KIND:-kafka}"

cat > "${OUT}" <<'HEADER'
# Auto-generated by gen_robots_compose.sh — one service per robot.
services:
HEADER

if [[ "${TOPOLOGY}" == "edge" ]]; then
    # Edge mode: each robot uses the project Dispatcher image so it can run
    # its own sink alongside the publisher.  No `depends_on: sink` because
    # there is no central sink in this topology.
    for ((i = 1; i <= N; i++)); do
        cat >> "${OUT}" <<EOF
  robot_${i}:
    image: ros2-kafka-dispatcher:scaling
    network_mode: host
    ipc: host
    environment:
      ROBOT_ID: "${i}"
      BAG_PATH: "/data/bag"
      RATE_HZ: "10"
      ROS_DOMAIN_ID: "42"
      MSG_TYPE: "\${MSG_TYPE:-multi}"
      SINK_KIND: "${SINK_KIND}"
      BROKER_HOST: "localhost"
    volumes:
      - "\${BAG_PATH:?BAG_PATH is required}:/data/bag:ro"
      - ./edge_entrypoint.sh:/usr/local/bin/edge_entrypoint.sh:ro
      - ./robot_replay.py:/app/robot_replay.py:ro
    entrypoint: ["/usr/local/bin/edge_entrypoint.sh"]
    depends_on:
      - broker
EOF
    done
else
    # Gateway mode (default): tiny publisher-only image; depends on the
    # central sink declared in compose.kafka.yml / compose.mqtt.yml.
    for ((i = 1; i <= N; i++)); do
        cat >> "${OUT}" <<EOF
  robot_${i}:
    image: ros2-scaling-robot:local
    network_mode: host
    ipc: host
    environment:
      ROBOT_ID: "${i}"
      BAG_PATH: "/data/bag"
      RATE_HZ: "10"
      ROS_DOMAIN_ID: "42"
      MSG_TYPE: "\${MSG_TYPE:-navsatfix}"
    volumes:
      - "\${BAG_PATH:?BAG_PATH is required}:/data/bag:ro"
    depends_on:
      - sink
EOF
    done
fi

echo "[gen_robots_compose] wrote ${N} robot services (topology=${TOPOLOGY}, sink_kind=${SINK_KIND}) to ${OUT}"
